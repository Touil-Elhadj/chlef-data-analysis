 *  personal-survey/iotf.php
 *  Classification IOTF (Cole 2000+2007) pour 2-18 ans + OMS pour adultes
 *  Extrait depuis save.php et étendu pour les âges >18 (utilise les
 *  seuils OMS standards adultes).
